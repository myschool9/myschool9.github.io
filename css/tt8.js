function getColor(x) {
	if (x === '') return sColor1;
	if (typeof x === 'number') {
		if (x === currTime) return sColorCurr;
		x = (Math.trunc(x / 10));
	} else {
		x = Number(x[0]);
	}
	return (isNaN(x) || x % 2 === 1) ? sColor2 : sColor1;
}
	
