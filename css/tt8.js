function isWorkDay(num) {
	return num < 50 && num % 10 >= 1 & num % 10 <= 7;	//////////////////////////////////////////////
}
function isGrp59(grp) {
	return grp.length === 3 && grp[0] >= '5' && grp[0] <= '9';
}
function isGrp19(grp) {
	return grp.length === 3 && grp[0] >= '1' && grp[0] <= '9';
}

