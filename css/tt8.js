function Unique(col) {
	let list = [];
	let myList = [];
	if (col === 'day') col = 'num';
	for (let row of a) {
		if (col === 'grp' && isGrp19(row[col]) ||
			col === 'kab' && row[col] !== kabFalse ||
			col === 'num' && isWorkDay(row[col]) ||
			col === 'tch') 
			list.push(row[col]);
	}
	myList = [...new Set(list)].filter(Boolean);
	if (col === 'num') {
//		//////////////////////////////////////////////////////////		myList.push(0);
		myList.sort((x, y) => x - y);
		myList.forEach((num, index) => {myList[index] = DAYS_LONG[num];});
	} else {
		myList.sort((x, y) => MySort(x, y));
	}
	return myList;
}

