function GetCurrentTime() {
	let d = new Date();
	let day = d.getUTCDay() - 1;
	if (day > 4 || day < 0) return 1;
	
	let m = new Date();
	let g = new Date();
	let min = 60 * g.getHours() + m.getMinutes();
	let i = lessonsTime.findIndex(x => x > min);
	
	return 10 * day + i + 1;	////////////////////////////////////////
}

