function touchEnd(n) {
	let dX = event.changedTouches[0].clientX - startX;
	let dY = event.changedTouches[0].clientY - startY;
	if (dX > 50 && Math.abs(dX) > Math.abs(dY)) HTMLFilter(n - 1);
	if (dX < -50 && Math.abs(dX) > Math.abs(dY)) HTMLFilter(n + 1);
}

