        if (row.chZn !== 1 || rowPrev.chZn !== -1) {
          color = sErrorColor;
        }
        if (row.chZn === 1 && rowPrev.chZn === -1 && row.sub === rowPrev.sub 
          && row.grp === rowPrev.grp && row.tch === rowPrev.tch) {
            color = sErrorColor;
        }
